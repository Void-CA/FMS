import streamlit as st

# Título principal
st.title("Explora la Freestyle Master Series (FMS) como nunca antes")

# Introducción
st.markdown(""" 
¡Bienvenid@!  
Esta app está hecha para todos los que amamos el freestyle y queremos ver más allá de las batallas. ¿Qué tan consistentes han sido los MCs? ¿Hay patrones ocultos en su desempeño? Aquí encontrarás respuestas (¡y quizás algunas sorpresas!).  

Como fan del freestyle y apasionado por los datos, quise crear este espacio para combinar ambos mundos. Mi idea es que todos podamos explorar los datos de FMS y aprender algo nuevo sobre el arte detrás de las barras.

### ¿Qué encontrarás aquí?
- **Estadísticas descriptivas:** Mira cómo les ha ido a los MCs en general o por temporada.  
- **Tendencias y evolución:** Descubre cómo han cambiado los desempeños a lo largo de los años.  
- **Rendimientos inesperados:** ¿Algún MC tuvo un rendimiento fuera de lo común?  
- **Comparaciones:** Enfrenta a tus MCs favoritos y ve quién ha tenido mejores resultados.  

### ¿Cómo usar esta app?
1. Ve al menú y elige la sección que quieras explorar.  
2. Usa los filtros para personalizar gráficos y tablas.  
3. Disfruta los datos, y si descubres algo genial, ¡compártelo!

### Sobre los datos
Los datos incluyen temporadas de FMS de España, Argentina, Chile, México, Perú, Colombia y Caribe. Analizamos puntos de batalla (PTB), victorias, derrotas, réplicas y más. Estos vienen del sitio oficial de FMS y otras fuentes confiables.

Algunas preguntas interesantes que podrías resolver:
- ¿Quién domina consistentemente cada temporada?  
- ¿Existen diferencias de votacion entre ligas?  
- ¿Hay patrones en cómo cambian los puntajes con el tiempo?  

Así que, ¿qué esperas? Dale clic, explora y disfruta la ciencia detrás del arte.
""")
