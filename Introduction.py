import streamlit as st

# Título principal
st.title("Análisis Interactivo de la Freestyle Master Series (FMS)")

# Introducción
st.markdown("""
¡Bienvenido al análisis interactivo de la **Freestyle Master Series (FMS)**, una de las competencias más importantes en el mundo del freestyle rap!  
Este proyecto está diseñado para explorar de manera dinámica el desempeño de los MCs a lo largo de los años, identificar tendencias clave y descubrir patrones interesantes en los resultados.  

### ¿Qué encontrarás aquí?
- **Estadísticas Descriptivas**: Resúmenes del rendimiento de los MCs por año y en general.  
- **Tendencias Temporales**: Evolución del desempeño de los MCs en distintas temporadas.  
- **Rendimientos Atípicos**: Detecta MCs que destacaron por encima (o debajo) del promedio esperado.  
- **Comparaciones entre MCs**: Identifica quiénes han tenido el mejor desempeño relativo en diferentes temporadas.  

### ¿Cómo usar esta aplicación?
1. Usa el menú de navegación para seleccionar una sección de análisis.  
2. Filtra por MC, año o resultados específicos para personalizar tus gráficos.  
3. Explora las métricas y visualizaciones para obtener conclusiones valiosas.

### Sobre los datos
Los datos analizados incluyen puntos de batalla (PTB), resultados anuales, victorias, derrotas y más. A través de gráficos y tablas, podrás responder preguntas como:  
- ¿Quién ha sido el MC más consistente en las últimas temporadas?  
- ¿Existen diferencias significativas entre las ligas?  
- ¿Qué patrones emergen en el desempeño a lo largo del tiempo?  

¡Explora, interactúa y encuentra la ciencia detrás del arte del freestyle!
""")
